// Docs/05 appendix generator: emits the enumerated token inventory and the
// contrast pairing table from the actual sources — `src/tokens/*.css` plus
// the PAIRINGS contract in build/token-lib.mjs — and maintains them between
// markers in docs/05-design-tokens.md. Modes:
//   node build/gen-token-appendix.mjs          rewrite the doc in place
//   node build/gen-token-appendix.mjs --check  exit 1 if the doc is stale (lint/CI)
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { PAIRINGS, loadTokenFiles, evaluate, ratio, splitArgs } from './token-lib.mjs';

const DOC = fileURLToPath(new URL('../docs/05-design-tokens.md', import.meta.url));
const BEGIN = '<!-- WELKIN:TOKEN-APPENDIX:BEGIN — generated by build/gen-token-appendix.mjs; do not edit by hand -->';
const END = '<!-- WELKIN:TOKEN-APPENDIX:END -->';

const SECTIONS = {
  'color.css': 'Colour',
  'type.css': 'Type',
  'space.css': 'Space',
  'radius-border-shadow.css': 'Radius, border, shadow',
  'motion.css': 'Motion',
  'focus-ring.css': 'Focus ring',
};

const files = loadTokenFiles();
const colorDecls = files.find((f) => f.file === 'color.css').decls;

// Primitives are the enumerated oklch ramps in color.css; everything else
// in src/tokens/ is semantic. Component tokens live in each component's
// file and join this inventory when components land.
const isPrimitive = (name) => /^--wel-(grey|blue|green|amber|red)-\d+$/.test(name);

const short = (name) => name.replace('--wel-color-', '');

// Worst-case (both schemes) ratio for a pairing, from the real token source.
function worstCase(fg, bg) {
  return Math.min(
    ...['light', 'dark'].map((s) =>
      ratio(evaluate(`var(${fg})`, s, colorDecls), evaluate(`var(${bg})`, s, colorDecls))
    )
  );
}

// Pairing partners per colour token, for the inventory's Pairings column.
const partners = new Map();
for (const [fg, bg] of PAIRINGS) {
  const r = worstCase(fg, bg).toFixed(1);
  if (!partners.has(fg)) partners.set(fg, []);
  if (!partners.has(bg)) partners.set(bg, []);
  partners.get(fg).push(`on \`${short(bg)}\` ${r}`);
  partners.get(bg).push(`under \`${short(fg)}\` ${r}`);
}

function initialValue(raw) {
  // Multi-line declarations (the accent-contrast flip) must stay one table cell.
  raw = raw.replace(/\s+/g, ' ');
  if (raw.startsWith('light-dark(')) {
    const [a, b] = splitArgs(raw.slice('light-dark('.length, -1));
    return `light \`${a}\` / dark \`${b}\``;
  }
  return `\`${raw}\``;
}

const lines = [];
lines.push('### Inventory');
lines.push('');
for (const { file, decls, regs } of files) {
  lines.push(`#### ${SECTIONS[file] ?? file} (\`src/tokens/${file}\`)`);
  lines.push('');
  lines.push('| Token | Tier | `@property` | Initial value | Pairings |');
  lines.push('|-------|------|-------------|---------------|----------|');
  for (const [name, raw] of decls) {
    const tier = isPrimitive(name) ? 'primitive' : 'semantic';
    const reg = regs.get(name);
    const syntax = reg ? `\`${reg.syntax}\`` : '—';
    const pairs = partners.get(name)?.join('; ') ?? '—';
    lines.push(`| \`${name}\` | ${tier} | ${syntax} | ${initialValue(raw)} | ${pairs} |`);
  }
  lines.push('');
}
lines.push(
  'No component-tier tokens exist yet; they are defined in each component\'s file and join this inventory as components land.'
);
lines.push('');

lines.push('### Pairing table');
lines.push('');
lines.push(
  'Every foreground/background combination components are allowed to use. Ratios are computed from the token source in both schemes by the same evaluator that gates CI (`build/check-contrast.mjs`); **worst** is the lower of the two and must meet **min**.'
);
lines.push('');
lines.push('| Foreground | Background | Min | Light | Dark | Worst | Use |');
lines.push('|------------|------------|-----|-------|------|-------|-----|');
for (const [fg, bg, min, why] of PAIRINGS) {
  const rs = Object.fromEntries(
    ['light', 'dark'].map((s) => [
      s,
      ratio(evaluate(`var(${fg})`, s, colorDecls), evaluate(`var(${bg})`, s, colorDecls)),
    ])
  );
  const worst = Math.min(rs.light, rs.dark);
  lines.push(
    `| \`${short(fg)}\` | \`${short(bg)}\` | ${min}:1 | ${rs.light.toFixed(2)}:1 | ${rs.dark.toFixed(2)}:1 | ${worst.toFixed(2)}:1 | ${why} |`
  );
}
lines.push('');
lines.push(
  `Totals: ${files.reduce((n, f) => n + f.decls.size, 0)} tokens, ${PAIRINGS.length} pairings × 2 schemes.`
);

const generated = lines.join('\n');

const doc = readFileSync(DOC, 'utf8');
const beginIdx = doc.indexOf(BEGIN);
const endIdx = doc.indexOf(END);
if (beginIdx === -1 || endIdx === -1 || endIdx < beginIdx) {
  console.error(`marker block not found in ${DOC} — expected ${BEGIN} … ${END}`);
  process.exit(1);
}

const current = doc.slice(beginIdx + BEGIN.length, endIdx).replace(/^\n+|\n+$/g, '');
const next = doc.slice(0, beginIdx + BEGIN.length) + '\n\n' + generated + '\n\n' + doc.slice(endIdx);

if (process.argv.includes('--check')) {
  if (current !== generated) {
    console.error(
      'docs/05 token appendix is stale — regenerate with `node build/gen-token-appendix.mjs`'
    );
    process.exit(1);
  }
  console.log('token appendix up to date');
} else {
  writeFileSync(DOC, next);
  console.log(`token appendix written to docs/05-design-tokens.md`);
}
